def divisao(a,b):
    c = a / b
    d = a // b
    e = a % b
    print('A divisão entre {} e {} é: {}'.format(a,b,c))
    print('A divisão arredondada entre {} e {} é: {}'.format(a,b,d))
    print('O resto da divisão entre {} e {} é: {}'.format(a,b,e))

